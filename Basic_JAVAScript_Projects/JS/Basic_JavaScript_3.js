


   
function frog(num1, num2) {
    return num1 + num2;
}

function worm(num1, num2) {
    return num1 - num2;
}

function rabbit(num1, num2) {
    return num1 * num2;
}

function shark(num1, num2) {
    return num1/ num2;
}

function platipus(num1, num2) {
    return num1/ num2 + num1;
}

function remainder(num1, num2) {
    return num1 % num2;
}

function negation(num1) {
    return -num1;
}

function increment(num1) {
    return num1++;
}

function decrement(num1) {
    return num1--;
}
